package assignment_solutions.assignment03_arithmetic_operators;

/*
 Question-1

Write a program to convert gallons into Liters and display it.

        Sample output:

       15 gallon is 56.7812 liter
 */

public class Question1 {

	public static void main(String[] args) {
		
		double gallon = 25;
		
		double liter = gallon * 3.7854;
		
		System.out.println(gallon + " gallon is " + liter + " liter");


		System.out.println("*************************************************************");


		double gallonx= 35;

		double literx = gallonx * 3.7854;

		System.out.println(gallonx + " gallon is " + literx + " liter");



	}

}
